라. 판매자가 회사에 대하여 미결제수수료가 있는 경우, 그 수금을 위하여 수금완료 시까지 해당 판매자의 아이디(ID), 성명 또는 상호, 연락처, 주소 등 최소한의 필요정보를 보관합니다.
마. 기타 정보수집에 관한 동의를 받을 때 또는 고객이 명시적으로 그 보유기간을 정한 경우(다만 법률상 의무보존기간은 초과하지 아니합니다.)에는 그 보유기간까지 회원 정보를 보관합니다.
바. 회원이 자발적으로 회원탈퇴한 후 회원 재가입, 임의해지 등을 반복적으로 행하여 회사가 제공하는 할인쿠폰, 이벤트 혜택 등으로 경제적 이익을 취하거나 이 과정에서 명의를 무단으로 사용하는 등 편법과 불법 행위를 하는 회원을 차단하고자 회사는 회원의 개인정보를 7일이 지난 후 파기 합니다.
