제8조 (판매자의 이용계약의 해지)
1. 판매자는 언제든지 판매자 매니저를 통해 케이마켓(쇼핑몰 사업부문)에게 해지의사를 통지함으로써 이용계약을 해지할 수 있으며 이와 별도로 CBT서비스 이용 해지만 진행 할 수도 있습니다. 다만, 판매자는 해지의사를 통지하기 전에 모든 상품의 판매 절차를 완료, 철회 또는 취소해야만 합니다.
이 경우 판매의 철회 또는 취소로 인한 불이익은 판매자 본인이 부담하여야 합니다.
