나. 구매계약이 체결된 재화 또는 용역의 배송
3. 티켓예매서비스 또는 ENT서비스
가. 재화 또는 용역에 대한 정보 제공 및 구매계약의 중개
나. 공연 등에 대한 관람서비스의 판매
4. 여행 서비스(개정 2019.08.30)
5. 기타 회사가 정하는 업무 등
② 쇼핑서비스의 경우 회사는 구매자와 판매자간 거래의 안정성 및 신뢰성을 증진시키는 도구만을 제공하며, 구매자와 판매자간에 성립된 거래와 관련된 책임은 거래당사자들 스스로가 부담하여야 합니다.
제5조 (정보의 제공)
① 회사는 회원이 서비스 이용 중 필요하다고 인정되는 다양한 정보를 전자우편 또는 서신우편 등의 방법으로 회원에게 제공할 수 있습니다.
② 회사는 거래실적 등에 따라 회원의 신용등급을 정하여 각각의 혜택을 부여할 수 있으며 이에 관해 회사는 별도 공지할 수 있습니다.
다만 제14조에 따라 회원자격이 정지되는 경우 신용등급 등이 혜택이 소멸되고, 재가입 등의 경우에도 복원되지 아니합니다.
③ 회사는 본 약관에 따라 회원의 관련법령 등에 위반이 있음이 확인되거나 현저한 가능성이 있는 경우 회원자격정지 및 관계기관의 정보제공(신고) 등의 절차를 시행할 수 있습니다.
④ 회사는 회사 또는 ㈜케이마켓가 직접 운영하는 제2조 제1항의 사이트 이외에 다른 경로로 접근하거나 다른 도메인 등으로 접속하여 오류로 연결되는 이용자의 이용에 대하여 신뢰성을 보장하지 아니합니다.
제6조 (회사의 의무)
① 회사는 법령과 이 약관이 금지하거나 공서양속에 반하는 행위를 하지 않으며 이 약관이 정하는 바에 따라 지속적이고, 안정적으로 서비스를 제공하는 데 최선을 다 합니다.
② 회사는 이용자가 안전하게 인터넷 서비스를 이용할 수 있도록 이용자의 개인정보(신용/금융정보 포함)보호를 위한 보안시스템을 갖추어야 합니다.
③ 회사는 관련법령을 준수하고, 회원을 위한 고객 상담센터를 운영하여 각종 민원의 해소를 위해 노력합니다
제7조 (이용자의 의무)
이용자는 다음 행위를 하여서는 안됩니다.
1. 신청, 변경시 허위내용의 등록 또는 타인의 개인정보, 신용 및 금융정보, 연락처, 아이디(닉네임 포함) 도용 및 임의사용
2. 회사에 게시된 정보를 변경하거나 고의로 변경을 시도하는 행위
3. 회사가 정한 정보 이외의 정보(바이러스 프로그램 등)의 송신 또는 게시
4. 회사 기타 제3자의 저작권 등 지적재산권에 대한 침해
5. 회사 기타 제3자의 명예를 손상시키거나 업무를 방해하는 행위
6. 외설 또는 폭력적인 메시지·화상·음성 기타 공서양속에 반하는 정보를 회사에 공개 또는 게시하는 행위
7. 회사의 서비스정책에 반하여 재판매 또는 허위매출 만을 위해 상품을 구매하는 행위
8. 회사가 이용자를 위해 시행하는 각종의 이벤트(할인쿠폰 등) 서비스를 회사 서비스정책에 맞지 않도록 사용하는 행위
9. 현금융통을 위해 신용카드를 사용하거나, 금감원, 수사기관 등의 제재를 받을 수 있는 사항의 거래요청, 회사의 정당한 관련서류 제출요구에 응하지 않는 행위
10. 회사의 고객불만사항 처리절차 중 통상적인 범위를 현저히 벗어나는 욕설, 성적비하 등의 비 인격적 언행표출 및 위력과시 등의 공포감 유발 등을 수 차례 반복하는 행위
11. 회사를 통해 알게 된 재화 및 용역의 대상을 판매자와 직거래하는 행위
12. 법령에 따라 온라인 유통이 금지되거나 제한되는 재화, 용역을 구매하려는 행위
제8조 (회원의 ID 및 비밀번호에 대한 의무)
① 회원은 관련법령에 따라 회원정보 ID와 비밀번호에 관한 관리책임을 가집니다.
② 회원은 자신의 ID 및 비밀번호를 제공하거나 임의 사용하는 등 제3자에게 이용하게 해서는 안됩니다.
③ 회원이 자신의 ID 및 비밀번호 또는 개인정보 등이 도난 당하거나 제3자가 사용하고 있음을 인지한 경우에는 바로 회사에 알리고 회사의 안내에 따라야 합니다.
제9조 (회원의 게시물)
① 회사는 회원이 게시하거나 등록(링크를 포함)하는 서비스의 내용물이 다음 각 호에 해당한다고 판단되는 경우에 사전통지 없이 삭제할 수 있으며, 기타 필요한 조치를 취할 수 있습니다.
1. 다른 회원 또는 제3자를 비방하거나 명예를 훼손시키는 내용인 경우
2. 공공질서 및 미풍양속에 위반되는 내용일 경우
3. 범죄행위와 결부된다고 인정되거나 종교적, 정치적, 사회적 분쟁을 일으킬 수 있는 경우
4. 회사의 저작권, 제3자의 저작권 등 기타 권리를 침해하는 내용인 경우
5. 회사에서 규정한 게시기간을 초과한 경우
6. 회원이 회사 사이트 및 게시판에 음란물을 게재하거나 음란사이트를 링크하는 경우
7. 해당화면과 객관적인 없는 내용(다른 광고문구 등)
8. 티켓 등의 재판매를 위한 매매양도를 유도하는 경우
② 회사는 판매자 또는 이용자가 다른 판매자 또는 이용자가 게재한 정보, 자료, 사실의 정확성 등을 신뢰함으로써 입은 손해에 대하여 책임을 지지 않습니다.
제10조 (서비스 이용시간)
① 서비스의 이용은 회사의 업무상 또는 기술상 특별한 지장이 없는 한 연중무휴 매일 24시간을 원칙으로 합니다. 다만 정기 점검 등의 필요로 회사가 정한 날이나 시간은 제외됩니다.
② 회사는 서비스를 일정범위로 분할하여 각 범위 별로 이용가능 시간을 별도로 정할 수 있습니다. 이 경우 그 내용을 사전에 공지합니다.
제11조 (서비스의 중단)
① 회사는 컴퓨터 등 정보통신설비의 보수점검·교체 및 고장, 통신의 두절 등의 사유가 발생한 경우에는 서비스의 제공을 일시적으로 중단할 수 있습니다.
② 제1항에 의한 서비스 중단의 경우에 회사는 서비스 일시 중단 사실과 그 사유를 케이마켓 초기화면에 통지합니다.
③ 회사는 제1항의 사유로 서비스의 제공이 일시적으로 중단됨으로 인하여 이용자 또는 제3자가 입은 손해에 대하여 회사의 고의 또는 과실이 없는 경우에는 배상하지 아니합니다.
[제 3장 회원가입]
제12조 (가입신청)
① 이용자는 회사가 정한 가입 양식에 따라 회원정보를 기입한 후 이 약관에 동의한다는 의사표시를 함으로써 회원가입을 신청합니다.
② 회사는 제1항과 같이 회원으로 가입할 것을 신청한 이용자 중 다음 각 호에 해당하지 않는 한 회원으로 승낙합니다.
1. 가입신청자가 이 약관 제14조 제3항에 의하여 이전에 회원자격을 상실한 적이 있는 경우.
다만 회원자격 상실 후 3년이 경과한 자로서 회사의 회원 재가입 승낙을 얻은 경우에는 예외로 한다.
2. 등록 내용에 허위, 기재누락, 오기 등이 있는 경우
3. 기타 회원으로 등록하는 것이 회사의 기술상 현저히 지장이 있다고 판단되는 경우
4. 만14세 미만의 아동으로서 부모 등 법정대리인의 동의를 얻지 아니한 경우
③ 회원가입계약의 성립시기는 회사의 승낙이 회원에게 도달한 시점으로 합니다.
④ 회원은 개인정보 항목의 등록사항에 변경이 있는 경우 , 즉시 전자우편 기타 방법으로 회사에 대하여 그 변경사항을 알려야 합니다.
⑤ 법인은 사업자 회원으로 가입 신청하여 사용 가능하며, 개인의 경우에도 개인사업자로서 회사를 이용하기 위하여 가입할 경우에는 사업자 회원으로 가입 가능 합니다. 사업자 회원으로 가입하실 경우 사업자등록번호 기준 1개의 ID만 등록 가능합니다.
⑥ 탈퇴 후 회원 재가입, 임의 해지 등을 반복하거나 다수의 아이디(ID)로 가입하여 회사가 제공하는 각종 할인쿠폰, 이벤트 혜택 등으로 경제적 이익을 편법으로 수취하고, 이 과정에서 명의를 무단으로 사용하는 등 편법과 불법 행위를 하는 회원을 차단하고자 회사는 회원이 탈퇴한 후 7일이 지나지 않으면 회원의 서비스 가입신청을 거부할 수 있습니다.
제13조 (어린이 회원의 가입에 대한 특칙)
① 만14세 미만의 아동의 개인정보의 수집절차는 본 회원약관의 [제4장 어린이 회원]의 내용에 따릅니다.
② 회사는 부모 등 법정대리인의 동의에 대한 확인 절차를 거치지 않은 만14세 미만의 아동에 대해서는 가입을 거절 또는 취소할 수 있습니다.
제14조 (회원 탈퇴 및 자격 상실 등)
① 회원은 회사에 언제든지 탈퇴를 요청할 수 있으며 회사는 즉시 회원탈퇴를 처리합니다.
다만, 해지의사를 통지하기 전에 모든 상품의 판매 및 구매 절차를 완료, 철회 또는 취소해야만 합니다.
이 경우 판매 및 구매의 철회 또는 취소로 인한 불이익은 회원 본인이 부담하여야 합니다.
② 회원이 다음 각 호의 사유에 해당하는 경우, 회사의 회원자격을 제한 및 정지시킬 수 있습니다.
1. 가입 신청 시에 허위 내용을 등록한 경우
2. 회사를 이용하여 구입한 재화·용역 등의 대금, 기타 회사의 이용에 관련하여 회원이 부담하는 채무를 기일에 지급하지 않는 경우
3. 다른 사람의 회사 이용을 방해하거나 그 정보를 도용하는 등 전자상거래 질서를 위협하는 경우
4. 회사를 이용하여 법령과 본 약관이 금지하거나 공서양속에 반하는 행위를 하는 경우
5. 타인의 개인정보를 도용 및 임의사용 하거나 연락처의 허위/도용 또는 고의로 회사 및 판매자와의 연락을 두절하는 경우
6. 기타 회사의 영업행위를 고의로 방해하는 경우
7. 본 약관 제7조 내지 제9조의 각 호에 해당하는 경우
③ 회사가 회원 자격을 제한·정지시킨 후, 동일한 행위가 2회 이상 반복되거나 30일 이내에 그 사유가 시정되지 아니하는 경우 회사는 회원자격을 상실시킬 수 있습니다.
④ 회사가 회원자격을 상실시키는 경우에는 회원등록을 말소합니다. 이 경우 회원에게 이를 통지하고, 회원등록말소 전에 소명할 기회를 부여합니다.
단, 회사는 제2항 각 호의 사유에 해당하는 행위를 한 회원에 대해서는 회원자격 상실일로부터 3년간 정보를 보유합니다.
⑤ 판매자 자격 제한 및 정지에 대한 규정은 오픈마켓 판매자약관에 준하여 적용됩니다.
⑥ 회원의 회원탈퇴, 회원자격상실 등으로 본 약관의 적용이 종료되는 경우 제5조 제2항의 신용등급혜택, 마일리지 점수(I-Point), 영화/공연/문화예매권 및 티켓서비스의 토핑(Toping)회원 서비스 등도 자동 소멸되고, 재가입시 복원되지 않습니다.
제15조 (회원에 대한 통지)
① 회사가 회원에 대한 통지를 하는 경우, 회원이 회사에 제출한 전자우편주소, 휴대폰(SMS, 알림톡 등)으로 할 수 있습니다.
② 회사는 불특정다수 회원에 대한 통지의 경우 1주일 이상 회사 게시판에 게시함으로써 개별 통지에 갈음할 수 있습니다.
다만 회원 본인의 거래와 관련하여 중대한 영향을 미치는 사항에 대하여는 개별통지를 할 수 있습니다.
[제 4장 케이마켓 어린이 회원]
제16조 (어린이 회원 가입)
① 어린이 회원은 회사가 정한 가입 양식에 따라 자신의 개인정보를 입력하고 어린이 회원 약관에 동의한다는 의사표시를 합니다. 그리고 보호자의 승낙을 받음을 증명하여 회사의 어린이 회원가입을 요청하게 됩니다.
② 어린이 회원은 반드시 회원 본인의 정보를 제공하여야 하며, 보호자의 승낙을 받고 이를 사이트에서 규정한 방침에 따라 확인하여야만 회사의 서비스를 이용할 수 있습니다.
회사는 어린이 회원이 제공한 정보 및 보호자의 승낙유무를 확인할 수 있으며, 회원과 보호자는 회사의 확인조치에 대해 적극 협조하여야 합니다.
③ 어린이 회원 가입계약의 성립시기는 회사의 승낙이 어린이 회원에게 도달한 시점으로 합니다.
④ 어린이 회원이 만14세에 도달하면 케이마켓의 일반 회원으로 변경됩니다.
제17조 (이용신청의 승낙과 제한)
① 회사는 제16조의 규정에 따른 어린이 회원의 가입계약 신청에 대하여 업무의 수행 및 기술상 지장이 없고, 정당한 보호자의 승낙을 얻은 것으로 확인될 경우 원칙적으로 어린이 회원의 서비스 이용을 승낙합니다
② 회사는 아래 사항에 해당하는 경우, 그 사항이 해결될 때까지 어린이 회원의 가입에 대한 승낙을 제한할 수 있습니다.
가. 회사가 설비의 여유가 없는 경우
나. 회사가 기술상 지장이 있는 경우
다. 기타 회사의 귀책사유로 이용승낙이 곤란한 경우
③ 회사는 아래 사항에 해당하는 경우, 이미 가입을 승낙하였더라도 회원자격을 제한시키거나 정지시킬 수 있습니다.
가. 어린이 회원이 가입 신청시에 허위 개인정보를 제공한 경우
나. 보호자의 승낙없이 가입 신청을 한 경우
다. 보호자의 승낙이 없었음에도 회사로 하여금 승낙이 있는 것처럼 믿도록 기망하였고, 이 기망사실을 회사가 알게 된 경우
라. 회사를 이용하여 법령과 공서양속에 반하는 행위를 하거나, 본 약관이 금지한 행위를 하는 경우
마. 기타 회사의 영업행위를 고의적으로 방해하는 경우
제18조(어린이 회원의 탈퇴 및 회원자격 상실)
① 어린이 회원 및 어린이 회원의 보호자는 언제든지 회사에 탈퇴를 요청할 수 있으며, 회사는 즉시 회원탈퇴를 처리합니다.
다만, 회원탈퇴 이전에 처리 중이던 서비스의 이용 및 상품 구매 등의 절차는 완료, 철회, 취소를 해야 하며, 이 경우 회사의 고의나 과실이 없는 한 회사는 철회 또는 취소로 인한 불이익을 책임지지 않습니다.
② 회사가 제 17조 제③항의 사유로 어린이 회원의 자격을 제한, 정지시켰을 때, 동일한 행위가 반복되거나, 30일 이내에 사유가 시정되지 아니하는 경우 회사는 어린이 회원의 자격을 상실시킬 수 있습니다.
제19조 (어린이 회원에 대한 통지)
① 회사는 어린이 회원에게 통지를 하여야 할 경우, 회원이 회사에 제출한 전자우편 주소 또는 보호자의 전자우편주소로 통지할 수 있습니다.
② 어린이 회원의 서비스 이용 및 구매 행위시에 회사는 필요하다고 생각되는 경우, 어린이 회원의 보호자에게도 통지를 할 수 있습니다.
제20조 (어린이 회원의 의무)
① 어린이 회원은 다음의 행위를 하여서는 안됩니다.
가. 신청 또는 변경 시 허위 내용 혹은 보호자의 동의를 얻은 것으로 오인하도록 하는 행위
나. 회사가 정한 정보 이외의 정보(컴퓨터 프로그램 등)의 송신 또는 게시
다. 회사와 기타 제3자의 저작권 등 지적재산권에 대한 침해 행위
라. 회사와 기타 제3자의 명예를 손상시키거나 업무를 방해하는 행위
마. 폭력적, 외설적인 메시지, 화상, 음성 기타 공서양속에 반하는 정보를 회사에 공개하거나 게시하는 행위
바. 회사에 게시된 정보를 변경하거나 무단 도용하는 행위
② 어린이 회원은 회사의 동의가 없는 한, 서비스의 이용권한 및 이용계약상의 지위를 다른 사람에게 양도하거나 제공할 수 없습니다.
제21조 (특칙)
회사는 어린이 회원의 가입 및 이용에 대하여 본 장에서 규정하고 있지 않은 부분은 케이마켓 회원 이용약관을 따릅니다.
[제 5장 상품거래의 일반]
제22조 (구매신청)
이용자는 사이트 상에서 다음의 방법에 의하여 구매를 신청합니다.
1. 재화 또는 용역의 선택
2. 이용자 정보의 확인 및 배송정보, 결제 방법의 선택
3. 구매정보에 대한 확인 및 회사 또는 판매자에 대한 승낙의 요청 등
제23조 (미성년자 구매계약에 관한 특칙)
회사는 만19세 미만의 미성년 회원이 유료서비스를 이용하거나, 구매계약을 체결할 경우, 사전에 부모 등 법정대리인의 동의를 얻거나, 사후에 추인을 얻어야 합니다.
다만 관련법령에서 달리 정함이 있는 사항은 그에 따릅니다.
제24조 (계약의 성립)
판매자 또는 회사는 이용자의 구매신청에 대하여 다음 각 호에 해당하지 않는 한 승낙합니다.
1. 신청 내용에 허위, 기재누락, 오기가 있는 경우
2. 미성년자가 담배, 주류 등 청소년보호법에서 금지하는 재화 및 용역을 구매하거나 법령이 정하는 연령제한을 위반하는 경우
3. 구매신청 물품의 품절이나 기타 구매신청에 승낙하는 것이 회사의 기술상 현저히 지장이 있다고 판단하는 경우
4. 기타 제반 법령 및 정부의 가이드라인에 위반되거나 제7조 내지 제9조에 위반되는 경우 등
제25조 (대금지급방법)
① 회사에서 구매한 재화 또는 용역에 대한 대금지급방법은 다음 각 호 중 하나 또는 그 이상의 방법으로 결제할 수 있습니다.
1. 현행법령에 따른 각종 계좌이체
2. 각종 신용카드결제
3. 온라인무통장입금
4. 관련법령에 따라 승인/유통되는 전자금융거래 수단에 의한 결제
5. 회사가 인정하는 도서·문화상품권 등의 상품권에 의한 결제
6. 휴대폰결제
7. 수령 시 상품대금 또는 배송비용 지급
8. 기타 회사가 정하는 결제수단
② 구매대금의 결제와 관련하여 이용자가 입력한 정보 및 그와 관련된 책임은 이용자에게 있으며, 재화 또는 용역의 청약 후 합리적인 일정 기간 내에 결제가 이루어 지지 않는 경우 회사는 이에 해당주문을 취소할 수 있습니다.
③ 회사는 구매자의 결제수단에 대하여 정당한 사용권한 보유여부를 확인할 수 있으며 필요한 경우 해당 거래진행의 정지 및 소명자료의 제출을 요청할 수 있습니다.
[제 6장 쇼핑서비스]
제26조 (대리 및 보증의 부인)
① 회사가 제공하는 오픈마켓 서비스는 온라인상의 거래 환경장소를 제공하고 기타 부가정보를 제공함에 그치는 것으로 판매자와 구매자 상호간의 거래와 관련된 판매진행의 관리, 구매자에 대한 거래이행, 물품배송, 청약철회 또는 반품, 물품하자로 인한 분쟁해결 등 필요한 사후처리는 거래당사자인 판매자와 구매자들이 직접 수행하여야 합니다. 회사는 이에 대하여 원칙적으로 관여하지 않으며 어떠한 책임도 부담하지 않습니다.
② 회사의 오픈마켓 서비스를 통하여 이루어지는 판매 및 구매와 관련하여 판매 또는 구매의사의 여부 및 진정성, 게시된 상품의 품질, 완전성, 안전성, 적법성 및 여타의 권리관계에 대한 비침해성, 판매회원이 입력한 정보 및 그 정보를 통하여 링크된 Website의 게시물의 진실성 등에 대한 보증을 하지 아니하며, 그와 관련된 일체의 책임과 위험은 게시한 회원이 부담합니다.
③ 회사의 오픈마켓 서비스는 전자상거래 등에서의 소비자보호에 관한 법률에 의하여 통신판매 중개자의 지위를 갖습니다.
④ 회사는 구매자가 판매자와 직거래를 하는 경우 회사의 결제대금예치업무 등 관련 법령 및 본 약관(회사의 개별 약관 포함)에서 규정하는 제반 구매자 보호를 적용하지 않습니다.
제27조 (수신확인통지·구매신청 변경 및 취소)
① 회사는 구매자의 구매신청이 있는 경우 구매자에게 수신확인통지를 합니다.
② 수신확인통지를 받은 구매자는 의사표시의 불일치 등이 있는 경우에는 수신확인통지를 받은 후 즉시 구매신청 변경 및 취소를 요청할 수 있습니다.
③ 판매자 및 회사는 배송 전 구매자의 구매신청 변경 및 취소 요청이 있는 때에는 지체 없이 그 요청에 따라 처리합니다.
④ 회사는 구매자가 구매신청 후, 그 신청일로부터 7일 이내에 상품대금에 대해 입금이 확인되지 않을 경우 별도 의사확인 절차 없이 구매계약을 해제하는 것으로 처리할 수 있습니다.
제28조 (배송)
① 판매자는 구매자와 재화 등의 공급시기에 관하여 별도의 약정이 없는 이상, 이용자가 청약을 한 날부터 7일 이내에 재 화 등을 배송할 수 있도록 주문제작, 포장 등 기타의 필요한 조치를 취합니다. 다만, 판매자가 이미 재화 등의 대금의 전부 또는 일부를 받은 경우에는 대금의 전부 또는 일부를 받은 날부터 3영업일 이내에 재화 등의 공급을 위하여 필요한 조치를 취합니다. 이때 판매자는 구매자가 재화 등의 공급 절차 및 진행 사항을 확인할 수 있도록 적절한 조치를 합니다.
② 판매자는 구매자가 구매한 재화에 대해 배송수단, 수단별 배송비용 부담자, 수단별 배송기간 등을 명시합니다. 만약 판매자의 고의 또는 과실로 약정 배송기간을 초과한 경우에는 그로 인한 구매자의 손해를 배상할 수 있습니다. 다만 판매자가 고의,과실이 없음을 입증한 경우에는 그러하지 아니합니다.
③ 공휴일 및 기타 휴무일 또는 천재지변 등의 불가항력적 사유가 발생하는 경우 그 해당기한은 배송소요기간에서 제외합니다.
제29조 (환급, 반품 및 교환)
① 판매자는 구매자가 구매를 신청한 재화 또는 용역이 품절 등의 사유로 재화의 인도 또는 용역의 제공을 할 수 없거나 공급하기 곤란함을 알았을 때에는 지체 없이 그 사유를 구매자에게 통지합니다. 이 경우 사전에 재화 또는 용역의 대금을 받은 경우에는 대금을 받은 날부터 회사 영업일 기준 3일 이내에 대금을 환급하거나 환급에 필요한 조치를 취합니다.
② 회사 오픈마켓을 통해서 구입하신 상품은 구매확정절차 후 회사를 통해 환급, 교환, 반품을 요청이 제한되며 판매자와 구매자가 직접 협의해서 처리해야 합니다.
③ 회사는 합리적인 기간이 경과한 후에도 구매자가 구매확정절차를 이행치 않는 경우 해당전자상거래의 계약이행이 정상적으로 종료된 것으로 간주하여 자동구매확정절차를 이행할 수 있으며 판매자에게 정상적으로 판매대금을 지급하는 등 통신판매 중개자의 역할을 수행할 수 있습니다. 단 자동구매확정까지의 합리적인 기간은 개별약관으로 정합니다.
④ 구매자는 제반 법령에 따라 결제대금예치업무(에스크로)의 보호를 회사에 요청할 수 있습니다.
⑤ 반품 또는 교환에 필요한 왕복배송비용 기타 필요한 비용은 귀책사유 있는 쪽에서 부담하며, 배송상의 문제로 구매자가 입은 손해에 대한 배상책임은 귀책사유 있는 배송업체를 지정한 판매자에게 있습니다.
⑥ 해외에서 국내로 상품을 배송하는 해외구매대행서비스의 경우 전자상거래 등에서의 소비자보호에 관한 법률 및 동법시행령에 의하여 반품 및 교환이 제한될 수 있습니다.
제30조 (청약철회 등)
① 판매자와 재화 등의 구매에 관한 계약을 체결한 구매자는 수신확인의 통지를 받은 날부터 7일 이내에는 청약의 철회를 할 수 있습니다.
② 구매자는 재화 등을 배송 받은 경우 다음 각 호의 1에 해당하는 경우에는 반품 및 교환을 할 수 없습니다.
1. 구매자에게 책임 있는 사유로 재화 등이 멸실 또는 훼손된 경우(다만, 재화 등의 내용을 확인하기 위하여 포장 등을 훼손한 경우에는 청약철회를 할 수 있습니다)
2. 구매자의 사용 또는 일부 소비에 의하여 재화 등의 가치가 현저히 감소한 경우
3. 시간의 경과에 의하여 재판매가 곤란할 정도로 재화 등의 가치가 현저히 감소한 경우
4. 같은 성능을 지닌 재화 등으로 복제가 가능한 경우 그 원본인 재화 등의 포장을 훼손한 경우
③ 제2항 제2호 내지 제4호의 경우에 판매자가 사전에 청약철회 등이 제한되는 사실을 구매자가 쉽게 알 수 있는 곳에 명기하거나 시용상품을 제공하는 등의 조치를 하지 않았다면 구매자의 청약철회 등이 제한되지 않습니다.
④ 구매자는 제1항 및 제2항의 규정에 불구하고 재화 등의 내용이 표시·광고 내용과 다르거나 계약내용과 다르게 이행된 때에는 당해 재화 등을 공급받은 날로부터 3월 이내, 그 사실을 안 날 또는 알 수 있었던 날부터 30일 이내에 청약철회 등을 할 수 있습니다.
제31조 (청약철회 등의 효과)
① 판매자가 구매자로부터 재화 등을 반환 받은 경우 회사는 3영업일 이내에 이미 지급받은 재화 등의 대금을 환급합니다. 이 경우 회사가 이용자에게 재화 등의 환급을 지연한 때에는 그 지연기간에 대하여 전자상거래 등에서의 소비자보호에 관한 법률시행령이 정하는 지연이자율을 곱하여 산정한 지연이자를 지급합니다.
② 회사는 위 대금을 환급함에 있어서 구매자가 신용카드 또는 전자금융거래법 등이 정하는 결제수단으로 재화 등의 대금을 지급한 때에는 지체 없이 당해 결제수단을 제공한 사업자로 하여금 재화 등의 대금의 청구를 정지 또는 취소하도록 요청할 수 있습니다.
③ 청약철회 등의 경우 공급받은 재화 등의 반환에 필요한 비용은 구매자가 부담합니다. 판매자는 구매자에게 청약철회 등을 이유로 위약금 또는 손해배상을 청구하지 않습니다.
다만 재화 등의 내용이 표시·광고 내용과 다르거나 계약내용과 다르게 이행되어 청약철회 등을 하는 경우, 재화 등의 반환에 필요한 비용은 판매자가 부담합니다.
④ 구매자가 재화 등을 제공받을 때 발송비를 부담한 경우에 판매자는 청약철회시 그 비용을 누가 부담하는지를 이용자가 알기 쉽도록 명확하게 표시합니다.
[제 7장 도서서비스]
제32조 (수신확인통지·구매신청 변경 및 취소)
① 회사는 구매자의 구매신청이 있는 경우 구매자에게 수신확인통지를 합니다.
② 수신확인통지를 받은 구매자는 의사표시의 불일치 등이 있는 경우에는 수신확인통지를 받은 후 즉시 구매신청 변경 및 취소를 요청할 수 있습니다.
③ 판매자 및 회사는 배송 전 구매자의 구매신청 변경 및 취소 요청이 있는 때에는 지체 없이 그 요청에 따라 처리합니다.
④ 회사는 구매자가 구매신청 후, 그 신청일로부터 7일 이내에 상품대금에 대해 입금이 확인되지 않을 경우 별도 의사확인 절차 없이 구매계약을 해제하는 것으로 처리할 수 있습니다.
제33조 (배송)
① 회사는 구매자가 구매한 재화에 대해 배송수단, 수단별 배송비용 부담자, 수단별 배송기간 등을 명시하며, 회사의 고의 또는 과실로 약정 배송기간을 초과한 경우에는 그로 인한 구매자의 손해를 배상할 수 있습니다.
② 공휴일 및 기타 휴무일 또는 천재지변 등의 불가항력적 사유가 발생하는 경우 그 해당기한은 배송소요기간에서 제외합니다.
제34조 (eBook 서비스)
① eBook은 인터넷을 통해 서비스를 제공합니다.
② 회사가 제공하는 eBook 뷰어 소프트웨어를 설치하여야 이용할 수 있습니다.
③ 콘텐츠는 회사가 콘텐츠 제공자로부터 사용권한을 부여 받은 것으로 콘텐츠 제공자 또는 원 권리자에 모든 권리가 귀속되며 저작권 법령 또는 관련 지적 재산권 법률 및 조약에 의해 보호됩니다.
④ 콘텐츠 제공자의 요구에 의해 일부 eBook은 해외 지역에서 구매가 제한될 수 있습니다.
⑤ 회원은 콘텐츠를 구매하기 전에 구매하려는 상품의 상세 내용과 조건을 정확하게 확인해야 합니다. 구매하려는 상품의 내용과 거래 조건을 확인하지 않고 구매하여 발생한 모든 손해의 책임은 회원 본인에게 있습니다.
⑥ eBook은 구매 후 승인된 회원ID로만 이용이 가능하고, 타ID로 사용권한을 양도하실 수 없습니다.
⑦ eBook 상품은 구매 후 다운로드 받거나 한번이라도 읽었을 경우, 이미 콘텐츠를 이용한 것으로 간주하고 주문 취소가 불가능합니다. 단, 아래의 경우에 대해서는 주문 취소 및 환불이 가능합니다.
- eBook 서비스 중 디지털 콘텐츠를 다운로드 받지 않거나 스트리밍 서비스를 통해 열람하지 않은 경우, 결제일로부터 7일 이내에 취소를 요청하면 결제금액 전액을 환불 받을 수 있습니다.
- eBook 및 서비스를 구매하였음에도 사용자 환경으로 이용이 불가하다고 판단할 경우
- 회사의 귀책사유로 인해 회원이 이용기간 동안 서비스를 제공받을 수 없게 되는 경우, 회사는 회원에게 그 기간만큼 동일, 유사한 콘텐츠를 제공합니다.
⑧ 회사는 다음 각호에 해당하는 경우를 부정 이용행위로 봅니다.
- 동일한 ID로 6대 이상의 기기(PC, 스마트폰, 태블릿PC, 전용 단말기 등)에서 동시 접속하여 서비스를 이용하는 경우
- 자신의 ID 및 전자책, 이용권, 쿠폰 등을 타인에게 판매, 대여, 양도하는 행위 및 이를 광고하는 경우
- eBook 이용시 상품 전부 혹은 일부를 복사, 복제 혹은 녹화하고 이를 배포하는 경우
- eBook 저작권을 보호하기 위해 적용된 DRM을 무력화하는 경우
- 상품을 불법적으로 다운로드 받거나 이를 타인에게 제공하는 경우
⑨ 회사는 전항에 따른 부정 회원을 발견할 경우, 다음 각 호에 다른 조치를 취할 수 있습니다.
- 전자우편, SMS 등 전자통신을 통해 부정이용을 알리고, 해당 ID에 대한 서비스 이용을 정지시킬 수 있으며, 부정 취득 및 사용된 콘텐츠 및 결제수단은 회수 조치 및 배상을 요구할 수 있습니다.
- 부정사용이 재차 발견될 경우 회사는 위반 내용 고지와 함께 필요시 법적인 대응을 할 수 있습니다. 회사는 회원에게 30일 간의 소명기간을 부여하고, 소명기간 동안 정당한 사유를 제시하지 못할 경우, 회원등록을 말소하고 구입한 콘텐츠 및 권한을 종료할 수 있습니다.
⑩ 회사는 아래와 같은 사유로 회원과 계약을 체결한 서비스의 내용이 변경되거나 서비스가 중단될 경우, 회원에게 미리 공지하여 서비스 이용에 차질이 없게 조치합니다.
- 회사가 제공하기로 회원과 계약을 체결한 서비스의 내용을 재화 등의 품절 또는 기술적 사양의 변경 등의 사유로 변경할 경우
- 회사와 계약한 콘텐츠 제공자와의 계약 종료 및 변경 등의 사유로 서비스의 내용이 변경되거나, 서비스가 중단될 경우
- 사업의 양도, 사업 종목의 전환, 사업의 포기, 업체 간의 통합 등의 이유로 서비스를 제공할 수 없게 되는 경우
⑪ 회사는 무료로 제공되는 서비스의 일부 또는 전부를 회사의 정책 및 운영의 필요 상 수정, 중단, 변경할 수 있으며, 이에 대하여 회원에게 별도의 보상을 하지 않습니다.
⑫ 회원 탈퇴 시 기존에 구매했거나 다운로드 했던 콘텐츠는 이용이 불가합니다.
제35조 (환급, 반품 및 교환)
① 회사는 구매자가 구매를 신청한 재화 또는 용역이 품절 등의 사유로 재화의 인도 또는 용역의 제공을 할 수 없거나 공급하기 곤란함을 알았을 때에는 지체 없이 그 사유를 구매자에게 통지합니다. 이 경우 사전에 재화 또는 용역의 대금을 받은 경우에는 대금을 받은 날부터 회사 영업일 기준 3일 이내에 대금을 환급하거나 환급에 필요한 조치를 취합니다.
② 다음 각 호의 경우에는 회사는 배송된 재화일지라도 재화를 반품 받은 다음 구매자의 요구에 따라 즉시 환급, 반품 및 교환 조치를 합니다.
1. 배송된 재화가 주문내용과 상이하거나 회사가 제공한 정보와 상이할 경우
2. 배송된 재화가 파손, 손상되었거나 오염되었을 경우 또는 객관적인 기준에 의한 파본인 경우
3. 재화가 광고에 표시된 배송기간보다 늦게 배송된 경우
③ 본 조 제2항 각 호에 해당하지 않는 사유로 배송된 재화를 반환하는 경우 반품 및 반품과 관련된 부대비용 일체를 구매자가 부담해야 합니다.
④ 음반, 만화, 잡지, 영상화보집 등과 같이 포장의 해체 후 단시간 내에 열람 및 청취 가능한 매체의 청약철회는 전2항에 해당하지 않는 경우 반품 및 환불이 불가합니다.
제36조 (청약철회 등)
① 회사와 재화 등의 구매에 관한 계약을 체결한 구매자는 수신확인의 통지를 받은 날부터 7일 이내에는 청약의 철회를 할 수 있습니다.
② 구매자는 재화 등을 배송 받은 경우 다음 각 호의 1에 해당하는 경우에는 반품 및 교환을 할 수 없습니다.
1. 구매자에게 책임 있는 사유로 재화 등이 멸실 또는 훼손된 경우(다만, 재화 등 의 내용을 확인하기 위하여 단순 외부포장 등을 훼손한 경우에는 청약철회가 가능하지만 제35조 제4항의 경우에는 그러하지 않습니다)
2. 구매자의 사용 또는 일부 소비에 의하여 재화 등의 가치가 현저히 감소한 경우
3. 시간의 경과에 의하여 재판매가 곤란할 정도로 재화 등의 가치가 현저히 감소한 경우
4. 같은 성능을 지닌 재화 등으로 복제가 가능한 경우 그 원본인 재화 등의 포장을 훼손한 경우
③ 제2항 제2호 내지 제4호의 경우에 판매자가 사전에 청약철회 등이 제한되는 사실을 구매자가 쉽게 알 수 있는 곳에 명기하거나 시용상품을 제공하는 등의 조치를 하지 않았다면 구매자의 청약철회 등이 제한되지 않습니다.
④ 구매자는 제1항 및 제2항의 규정에 불구하고 재화 등의 내용이 표시·광고 내용과 다르거나 계약내용과 다르게 이행된 때에는 당해 재화 등을 공급받은 날로부터 3월 이내, 그 사실을 안 날 또는 알 수 있었던 날부터 30일 이내에 청약철회 등을 할 수 있습니다.
제37조 (청약철회 등의 효과)
제31조의 규정을 준용합니다
[제 8장 티켓예매/ENT서비스]
제38조 (예매서비스의 제공)
회사는 전화와 인터넷, 예매처를 통해 공연, 영화, 스포츠, 이벤트 등의 예약서비스를 제공하며, 예약가능 기간은 이용자가 사용하는 예약서비스의 선택, 결제수단, 배송방법에 따라 다르며, 이는 회사 사이트 이용안내에 명시된 내용을 따릅니다.
제39조 (티켓 예매 수수료)
① 회사는 회사가 제공하는 예약 서비스에 대해 예매 수수료를 부과합니다. 공연/콘서트, 영화, 스포츠 및 전시/레저 각 예매 서비스의 경우 회원등급(일반/Toping회원) 및 예매방법 (인터넷/콜센터 등)에 따라 차등화된 예매 수수료를 받고 있으며, 예매 수수료 정책은 회사의 티켓예매서비스상에 별도로 게시할 수 있고 제15조에 따라 회원에 대한 통지 후 변경될 수 있습니다.
② 개별 공연별로 달리 정하지 않는 한 예매수수료는 제43조에 따른 취소의 경우에도 환급하지 아니합니다. (개정 2010.07.01)
제40조 (허위구매에 대한 규제)
① 회사는 선량한 이용자의 보호를 위하여 다음에 해당하는 고객에 대하여 일정한 기간을 정하여 예매제한 또는 해당 예매건에 대한 예매취소 조치를 취할 수 있습니다.
가. 부정한 방법을 통한 예매의 경우(예. 매크로 이용 등 비정상적인 방법을 통한 예매)
나. 반복적인 다량 구매 후 취소하는 경우(예. 재판매를 위하여 일정기간 동안 다량 구매 후 취소를 반복하는 경우)
다. 암표매매의 목적으로 구매하거나 암표매매를 이미 한 경우(예. 구매한 티켓을 회사의 판매가보다 높은 가격을 받고 타에 판매하는 경우)
라. 기타 이에 준하는 부정한 이용으로 판단되는 경우 (개정 2016.06.01)
② 상기 1항의 경우 회사는 해당 고객에게 그 즉시 1일 이상의 소명기한을 부여하며, 그 기한 내 고객으로부터 소명서를 제출할 경우 그 내용이 합당하다고 판단되면 해당 조치를 해지할 수 있습니다. (개정 2015.10.20)
③ 회원이 본 조에 해당하는 행위를 지속적으로 행함으로써 회사 또는 다른 이용자에게 피해를 주는 것으로 판단되는 경우 회사는 제14조에 따라 회원자격을 제한, 정지 및 상실시킬 수 있습니다. (개정 2015.10.20)
제41조 (티켓배송)
① 회사는 이용자가 구매한 재화에 대해 배송수단, 수단별 배송비용 부담자, 수단별 배송기간 등을 명시합니다. 만약 회사의 고의 또는 과실로 약정 배송기간을 초과한 경우에는 그로 인한 구매자의 손해를 배상합니다.
② 배송 소요 기간은 결제한 익일(신용카드 결제, 무통장입금은 입금 확인 익일)에 발송하며 도착일은 우편배송은 지방포함 10일 이내의 기준이 적용됩니다. 단, 공휴일 및 기타 휴무일 또는 천재지변 등의 불가항력적 사유가 발생하는 경우 그 해당기한은 배송 소요기간에서 제외하며, 개별적인 재화 및 용역에 따라 별도로 정할 수 있습니다.
③ 티켓 배송료는 달리 정함이 없는 한 이용자가 부담하며 그 요금은 체신청의 빠른 등기우편료 등 비용을 기준으로 합니다.
제42조 (환급, 반품 및 교환)
회사는 이용자가 구매 신청한 재화 또는 용역이 품절 등의 사유로 재화의 인도 또는 용역의 제공을 할 수 없거나 공급하기 곤란함을 알았을 때에는 지체 없이 그 사유를 구매자에게 통지합니다. 이 경우 사전에 재화 또는 용역의 대금을 받은 경우에는 대금을 받은 날부터 회사의 영업일 기준 3일 이내에 대금을 환급하거나 환급에 필요한 조치를 취합니다.
제43조 (예매티켓에 대한 취소, 환불)
① 구매자가 예매하신 티켓의 취소는 행사/판매 취소 마감시간 전까지 가능합니다.
② 회사는 판매 및 취소 마감 시간 기준을 개별적인 재화 및 용역에 따라 제 39조의 방법으로 달리 정할 수 있습니다.
③ 전항의 판매 및 취소 마감 시간은 행사 주최사 및 극장사정에 의해 변경될 수 있습니다.
④ 구매자는 회사가 지정한 예매처나 회사 직영점에서 제2항의 취소시한까지 방문하여 직접 취소할 수 있습니다.
⑤ 구매자는 티켓 취소시한까지 취소 절차를 취하지 않은 경우 회사의 티켓예매 서비스상에서 취소나 환불을 할 수 없으며, 영화의 경우 예매한 극장의 취소규정에 따릅니다.
⑥ 구매자는 전화로 예매한 경우 제 2항의 취소기한까지 전화로 취소할 수 있습니다.
⑦ 구매자가 배송서비스를 이용해 티켓을 수령한 경우 지정된 취소마감 시한까지 티켓을 회사로 반송, 도달하도록 하여야 취소가 가능합니다.
⑧ 회사는 관련법률에 따라 예매 후 7일이내에 취소하는 경우 취소수수료를 부과하지 아니합니다.
⑨ 제8항의 기간이외의 취소시에는 개별적인 재화 및 용역에 따라 취소수수료를 달리 정하고 해당 수수료를 공제할 수 있으며, 취소수수료 정책은 사전공지후 변경될 수 있습니다. 다만, 달리 정하지 않은 경우 아래 기준에 따릅니다
1. 제8항에도 불구하고 취소의 시점이 소비자분쟁해결기준의 기한에 해당하는 경우 소비자분쟁해결기준의 보상규정에 따릅니다.
2. 취소의 시점이 공연일로부터 10일이상 남은 경우에는 아래기준의 취소수수료를 부과합니다.
- 뮤지컬, 콘서트, 클래식 등 공연권 : 4,000원
- 연극, 스포츠, 전시 등 입장권 : 2,000원
- 단, 상기금액은 판매가격의 10%를 초과하지 아니합니다.(개정 2010.07.01)
⑩ 구매자가 예매티켓을 분실, 훼손한 경우 회사는 해당 예매취소 및 변경, 재발행을 허용하지 않을 수 있습니다.
제44조 (결제방법에 따른 취소 처리)
① 신용카드 결제자
1. 구매자는 예매당일 회사의 인터넷사업장 등을 통해 직접 취소가 가능합니다. 단, 예매시간을 계산하는 기준은 시간이 아닌 날짜를 기준으로 합니다.
2. 구매자가 예매한 티켓을 취소할 경우 소정의 취소 수수료 등의 기타 비용을 회사에 지불하여야 하며 회사는 예매티켓의 환급 금액에서 취소 수수료 등을 공제/상계하여 차액을 환불할 수 있습니다.
3. 예매 취소시 카드사의 부분취소 방식에 따라 기존 결제내역을 취소하고 최초 결제시의 동일카드로 취소 시점에 따라 취소수수료와 배송료를 재승인하거나, 취소수수료와 배송료를 공제 후 결제금액을 취소합니다. 복수결제(2가지 이상의 결제시스템)으로 결제를 한 경우에는 부분취소가 제한될 수 있습니다.
② 온라인무통장 입금 결제자
회사는 고객의 인터넷 예매 취소시 고객이 기재한 환불 은행계좌번호와 예금주에 따라 취소 수수료와 송금 수수료를 공제한 금액을 고객 환불 계좌에 입금합니다.
제45조 (배송방법에 따른 취소 처리)
① 우편배송 예매자
1. 회사가 티켓을 발송한 이후 취소하려는 경우 구매자는 취소 마감시간 전까지 해당티켓을 회사로 반송, 도달하도록 하여야 하며 부분 취소인 경우에도 취소마감시간까지 취소를 원하시는 티켓을 반송, 도달하도록 하여야 만 취소/환불됩니다.
2. 구매자가 티켓을 반송하는 경우 구매자의 예약번호, 연락처, 반송사유, 무통장 입금 고객의 경우에는 환불 받으실 은행명, 계좌번호와 예금주이름을 적어 티켓과 함께 회사에 도달토록 하여야 합니다.
② 현장수령 예매자
1. 구매자는 예매당일 회사의 인터넷사업장을 통해 직접 취소가 가능합니다
2. 예매당일 이후에도 취소가 가능하지만 제43조에 따른 취소수수료가 부과됩니다.(개정 2010.07.01)
