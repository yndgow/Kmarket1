③ 판매자가 비밀을 누설하거나 본 계약 이외의 목적으로 고객정보를 이용한 경우
④ 판매자가 본 계약상의 의무를 위반하여 케이마켓(쇼핑몰 사업부문)이 고객으로부터 중대한 클레임을 접수 받고 그 클레임이 확정된 경우
⑤ 판매자가 본 계약상의 의무를 위반하여 케이마켓(쇼핑몰 사업부문)이 그 개선을 요청하였으나 특별한 사유없이 이를 이행하지 아니한 경우
⑥ 판매자가 본 계약상의 동일한 의무를 3회 위반한 경우
⑦ 회사의 결제대금예치서비스를 이용하지 않고 판매자와 구매자간 직접 물품대금을 주고 받는 직거래 유도행위를 한 경우
