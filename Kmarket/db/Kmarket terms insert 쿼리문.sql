③ 회사는 회사와 이용자간 또는 쇼핑서비스의 오픈마켓의 경우 판매자와 구매자간에 발생한 분쟁과 관련하여 이용자의 구제신청이 있는 경우 이용자를 위하여 공정거래위원회 또는 분쟁조정기관의 조정신청을 안내할 수 있습니다.
