라. 등록이용료는 판매자가 판매자 S-Money로 결제하여야 합니다.
5. 부가서비스이용료
가. 부가서비스이용료는 판매성사 여부, 반품 여부와 관계없이 선불로 부과되며 환불되지 않습니다. 다만, 부가서비스의 서비스 개시일 이전에 취소를 하는 경우 환불이 가능하며, 케이마켓(쇼핑몰 사업부문)이 제공하는 서비스 또는 정보통신시스템의 하자로 인하여 물품등록이 정상적으로 이루어지지 않거나 상품판매가 취소되는 경우에는 부가서비스이용료를 환불합니다.
나. 부가서비스 이용료는 판매자가 판매자 S-Money로 결제할 수 있습니다.
다. 판매자 광고의 경우 별도의 개별약관의 적용을 받을 수 있습니다.
6. 판매이용료
가. 판매이용료는 각 상품 카테고리별로 정해진 기준으로 판매된 수량만큼 판매자에게 부과됩니다. 단, 케이마켓가 지원하는 할인이 적용된 상품에 대해서는 할인금액의 15%의 이용료가 추가로 부과됩니다.
나. 구매자가 신용카드로 결제한 경우의 신용카드 결제시스템 이용료는 카테고리별로 정해진 이용료에 포함되어 있습니다. 다만 무이자 할부 판매등을 판매자가 임의로 선택한 경우에는 해당 이용료를 부과합니다.
다. 판매이용료는 정산시 공제됩니다. 다만, 반품, 판매취소의 경우에는 판매이용료가 부과되지 않습니다.
7. 기타 이용료
가. 판매자가 케이마켓(쇼핑몰 사업부문)에서 공통으로 적용되는 무이자 할부판매 이외의 추가 무이자 할부 판매를 판매자 매니저를 통하여 케이마켓(쇼핑몰 사업부문)이 정한 양식에 의하여 임의로 선택한 경우에는 해당 이용료를 부과합니다.
나. 판매자가 케이마켓(쇼핑몰 사업부문)에서 공통으로 적용되는 적립금 이외의 추가 적립금을 판매자 매니저를 통하여 케이마켓(쇼핑몰 사업부문)이 정한 양식에 의하여 임의로 선택하여 부여하는 경우에는 해당 이용료를 부과합니다.
다. 기타 이용료는 정산시 공제 됩니다. 다만, 반품, 판매취소의 경우에는 기타이용료가 부과되지 않습니다.
8. 케이마켓(쇼핑몰 사업부문)은 필요한 경우 서비스이용료를 신설, 변경할 수 있으며, 신설 또는 변경사항은 제 10조의 통지방법에 따릅니다.
9. 케이마켓는 판매자와 협의하여 할인쿠폰을 발행 및 적용할 수 있고 구매자가 이를 사용하는 경우(단, 판매자가 회사와 협의없이 판매금액을 할인하는 경우는 제외함) 판매이용료는 할인쿠폰 금액을 차감한 판매금액을 기준으로 부과합니다.
10. 구매자가 I-Point를 사용하여 판매자의 상품을 결제하는 경우 케이마켓는 이용료 범위 내에서 I-Point 사용액만큼 이용료를 할인할 수 있습니다.
제18조 (매매 부적합 물품)
1. 다음 각 호의 물품은 매매 부적합 물품으로서 상품의 등록 및 판매를 금하며, 아래의 예시 항목 이외에도 관계법령 또는 일반관행, 사회통념에 따라
케이마켓(쇼핑몰 사업부문)이 개별적으로 매매부적합 여부에 대하여 우선적으로 판단 및 제재할 수 있습니다.
① 허위 또는 과장 광고한 물품
② 특허권, 상표권, 의장권, 실용신안권 등 타인의 지적재산권을 침해하는 물품
③ 법률상 통신판매가 금지된 물품 (예, 의약품, 주류(통신판매가 허용되는 전통주 제외), 담배, 군용물품, 무기류, 교정용 안경 및 컨택트렌즈 등)
④ 법령상 필요한 신고, 허가, 심의, 심사, 검사 등을 득하지 않은 물품
⑤ 정당한 소유권을 보유하지 않은 장물 혹은 습득물
⑥ 음란물, 유해약물 등 공공질서나 선량한 사회풍속에 위배되는 물품
⑦ 현금융통을 위한 신용카드거래, 광고, 홍보 등 판매 이외의 목적이 의심되는 물품
⑧ 상장주식 및 양도 또는 매매가 불가한 상품권, 홍보용 할인권 등의 상품권
⑨ 제작/유통이 허가되지 않은 MP3음반, 소프트웨어 기타 저작권 침해물품
⑩ 구매회원의 정당한 청약 철회권을 임의로 제한, 거부할 것이 명시된 상품
⑪ 관계기관에 허가/신고사항과 다른 명칭, 성분으로 거래되는 상품 및 수입/생산/판매/유통이 금지되거나 제한 되는 물품
⑫ 기타 전자상거래 방식으로 판매하는 것이 사회상규 또는 법률에 위배되는 물품.
2. 판매자의 고의 또는 과실로 매매 부적합 물품이 등록된 경우, 케이마켓(쇼핑몰 사업부문)은 즉시 해당 상품의 판매를 중지하고 이 사실을 판매자에 통보합니다. 고의에 의한 등록이라고 판단될 경우, 케이마켓(쇼핑몰 사업부문)은 본 계약을 해지할 수 있습니다.
3. 판매자의 고의 또는 과실로 매매 부적합 물품이 판매된 경우, 케이마켓(쇼핑몰 사업부문)은 즉시 해당 상품의 판매를 중지하고 즉시 본 계약을 해지합니다. 해당 상품의 판매가 법적인 처벌규정이 있는 경우, 케이마켓(쇼핑몰 사업부문)은 위 사실을 관계기관에 즉시 신고합니다.
4. 매매 부적합 물품의 판매대금은 법적인 규정이 있을 경우 이에 따르며 그렇지 아니할 경우에는 구매자에게 환불합니다. 판매된 물품은 법적인 규정이 있을 경우 이에 따르며 그렇지 아니할 경우에는 케이마켓(쇼핑몰 사업부문)이 회수하여 폐기합니다. 이 경우 판매자는 해당 물품에 대한 일체의 재산권을 주장, 행사할 수 없으며 회수 및 폐기에 소요된 비용 또한 판매자가 부담하여야 합니다.
5. 매매부적합 물품의 등록을 이유로 판매가 삭제, 취소되거나 중지된 경우 그 동록에 사용된 등록이용료 및 부가서비스 이용료는 환불되지 않으며, 판매자는 오픈마켓 서비스의 판매자 자격이 제한 될 수 있습니다.
6. 판매자는 매매 부적합 물품의 등록 및 판매에 대한 형사상 책임을 지며 이로 인하여 케이마켓(쇼핑몰 사업부문) 또는 제3자에게 발생한 손해에 대하여 민사상 배상을 하여야 합니다.
제19조 (상품판매절차)
1. 상품판매는 상품등록시 판매자가 선택한 판매기간까지 진행됩니다. 다만, 판매자가 기재한 모든 판매수량이 판매된 경우 또는 케이마켓(쇼핑몰 사업부문)에 의하여 직권으로 해당 판매가 취소되는 경우 등에는 조기에 종료될 수 있습니다.
2. 판매가 이루어지면 케이마켓(쇼핑몰 사업부문)은 판매자매니저 화면에 판매현황을 게시합니다.
3. 케이마켓(쇼핑몰 사업부문)은 구매자가 입금 또는 물품대금을 결제하면 즉시 판매자매니저를 통해 해당 사항을 게시합니다. 판매자는 구매자의 입금 여부를 판매자매니저 화면을 통해 확인할 수 있습니다.
제20조 (상품의 배송)
1. 판매자는 매일 1회 이상 판매자매니저에 접속하여 상품에 대한 주문여부와 그 내역을 주문금액이 입금된 때로부터 24시간 이내에 확인하여야 하며 그 사실을 케이마켓(쇼핑몰 사업부문)에 판매자 매니저를 통하여 통보하여야 합니다.
2. 판매자는 구매자가 주문금액을 입금한 때로부터 48시간 이내(영업일 기준)에 주문받은 상품을 발송하여야 합니다. 만약 그러하지 못할 경우에는 그 사유와 예상발송일을 판매자매니저를 통해 통보하여야 합니다. 단, 상품의 특성상 주문제작이나 설치준비 등에 시간이 소요되는 특수한 경우에는 사전에 판매자와 구매자의 상호간의 합의를 통해 별도의 약정에 따르며 CBT서비스 상품인 경우 케이마켓와 상호간의 합의를 통해 진행합니다.
3. 판매자는 배송과정에서 상품이 파손되지 않도록 적절한 포장을 하여야 하며''전자상거래 등에서의 소비자 보호에 관한 법률'' 제16조의 규정에 따라 소비자의 청약에 따라 재화 등을 공급하는 경우 그 내용을 기재한 서면(이하 "공급서"라 한다)을 재화 등에 첨부하여 소비자에게 송부하여야 합니다.
4. 판매자가 물품을 운송할 경우 구매자가 물품을 수령하기까지, CBT서비스 상품인 경우 케이마켓가 정한 수령지까지 위험은 판매자가 부담합니다. 이에 따라 판매자는 구매자가 하자(파손,멸실,부패 등)없는 물품을 수령할 수 있도록 물품의 포장, 운송업체의 선정, 기타 필요한 조치를 적정하게 하여야 합니다.
5. 판매자는 케이마켓(쇼핑몰 사업부문)이 지정하는 물류대행업체(이하 "전담택배사"라 합니다)를 통해 상품을 배송할 수 있으며, 판매자는 케이마켓(쇼핑몰 사업부문)이 도입하는 배송위치 확인 서비스 이용에 원칙적으로 동의하며, 전담택배사의 이용절차 및 배송위치 확인 서비스의 세부사항은 개별약관이나 약정에 따릅니다.
6. 상품을 발송한 경우, 판매자는 즉시 발송일시, 택배운송장번호, 수화물번호, 등기화물번호 등 적절한 코드를 판매자 매니저에 입력함으로써 이 사실을 케이마켓(쇼핑몰 사업부문)이 확인할 수 있도록 하거나, 케이마켓(쇼핑몰 사업부문)의 배송위치 확인 서비스에 따라 관련 정보를 입력/운영합니다. 판매자는 발송한 물품을 72시간 내에 고객이 수령할 수 있도록 책임을 다하여야 하며 구매자가 수령한 직후 이를 케이마켓(쇼핑몰 사업부문)에 통보하여야 합니다. 단 CBT서비스 상품의 경우는 예외로 합니다.
7. 오픈마켓서비스를 통하여 거래된 물품의 운송과 관련하여 판매자와 구매자, 운송업체, 금융기관 등과 사이에 분쟁 등이 발생한 경우에는 관련 당사자간에 해결하여야 합니다.
케이마켓(쇼핑몰 사업부문)은 이에 대하여 관여하지 않으며 어떠한 책임도 부담하지 않습니다.
제21조 (상품의 반품 및 교환)
1. 구매자의 상품수령후 7일이내 또는 구매자의 상품수령일을 확인할 수 없는 때에는 판매자가 상품 발송후 14일 이내에, 구매자가 반품 또는 교환을 요청할 경우 판매자는 반품 또는 교환을 해 주어야 합니다. 다만 구매자의 귀책사유로 상품이 훼손된 경우, 사용이나 일부 소비로 인하여 상품의 가치가 현저히 감소한 경우, 복제가능한 상품의 포장을 훼손한 경우 및 법령에 의하여 반품이 제한되어 있는 경우는 예외로 합니다. 또한 상품이 표시 또는 광고 내용과 다를 경우에는 상품수령 후 3개월 이내 또는 그 사실을 알게 된 날 또는 알 수 있었던 날로부터 30일 이내에 구매자가 반품 또는 교환을 요청하는 경우, 판매자는 반품 또는 교환을 해 주어야 합니다. 케이마켓(쇼핑몰 사업부문)은 반품 또는 교환이 결정된 즉시 판매자매니저 등을 통해 판매자에 그 상세한 내역 및 사유, 귀책소재 등을 판매자에 통보할 수 있습니다.
2. 다음 각 호와 같은 판매자의 귀책사유로 반품 또는 교환이 발생한 경우, 이에 소요된 비용은 판매자가 부담하여야 합니다.
가. 주문한 상품과 다른 상품을 배송한 경우
나. 하자 있는 상품을 배송한 경우
다. 배송과정에서 상품이 파손된 경우
라. 안전상의 결함으로 리콜하는 등 기타 판매자의 귀책사유에 해당한다고 판단되는 경우
3. 반품시 소요되는 운송료는 반품의 원인을 제공한 자가 부담함을 원칙으로 합니다.
4. 상품의 반품 및 교환은 다음의 규정에 따릅니다.
① 반품접수된 주문건에 대하여 케이마켓(쇼핑몰 사업부문)은 판매자 매니저 등을 통하여 판매자에게 통지하며 판매자는 상품을 회수받은 즉시 판매자 매니저를 통하여 회수완료를 통지하여야 합니다. 그렇지 않을 경우 5일 후 자동적으로 회수완료로 처리됩니다.
② 교환 및 재배송 비용은 판매자 귀책의 경우 교환완료일 기준으로 정산시 판매대금에서 차감합니다.
③ 교환의 경우 회수가 이루어지면 24시간 이내에 판매자는 교환된 상품을 구매자에게 발송합니다.
5. 회사의 오픈 마켓에 등록된 상품품질의 보증 및 A/S 조건과 기간 등을 판매자는 반드시 명시 및 설명해야 하며, 제시한 조건과 내용에 따라 품질보증 및 A/S를 자신의 비용과 책임으로 실시하여야 하며 법령에 비해 품질보증과 기간 등의 제반 조건 충분치 아니할 경우 법령의 기준에 따른 책임을 부담하여야 합니다. 이 조항은 오픈마켓에서 판매회원의 거래정지/탈퇴 시에도 적용합니다.
6. 21조 1항에 의거하여, 판매회원의 귀책사유로 다수의 취소 및 반품이 발생하는 경우, 카테고리 내 타상품보다 취소, 반품비율이 현저히 높은 경우, 회사는 판매회원의 물품판매의 제한, 물품 및 옵션 등록 가능개수 제한 등의 패널티를 부과할 수 있습니다.
제22조 (서비스 페널티 제도)
1. 판매자는 자신의 귀책사유로 고객서비스 상의 사고가 발생하였을 경우, 페널티 제도 또는 표준서비스 규칙에 따라 벌점 또는 경고 점수가 부과되며 세부내역은 별도의 표준서비스규칙에 따릅니다.
2. 동일한 주문에 대하여 제1항의 위반사항이 복수로 발생하였을 경우, 개별 페널티 점수를 합한 것으로 합니다.
3. 케이마켓(쇼핑몰 사업부문)은 판매자매니저를 통해 세부내역을 수시로 제공할 수 있습니다.
제23조 (우수판매자 우대제도)
케이마켓(쇼핑몰 사업부문)은 오픈마켓 서비스의 판매자 가운데 판매활동 및 구매만족도를 심사하여 우수한 판매자를 스타셀로로 선정하여 우대할 수 있으며, 스타셀러 우대정책의 구체적인 내용은 판매자매니저에 별도로 정함에 따릅니다.
제24조 (판매대금의 정산 및 지급)
1. 판매대금 정산은 다음과 같은 방식을 따릅니다.
① 판매대금 정산은 케이마켓(쇼핑몰 사업부문)이 판매자에 지급하여야 할 금액(이하 "입금대상금액"이라 합니다)을 정하는 절차 또는 결과를 말합니다.
② 케이마켓(쇼핑몰 사업부문)의 정산기간은 1일 단위로 하며, 익일 판매자 S-Money로 지급 합니다.
③ 지급된 판매자 S-Money는 바로 출금신청이 가능하며, 출금신청 후 금융영업일 기준 1일 이후에 판매자의 출금계좌로 현금이 지급됩니다.
④케이마켓(쇼핑몰 사업부문)은 전월1일부터 전월 말일까지 마감 후 매월 영업일 시작일 기준일로 판매자매니저를 통하여 세금계산서를 발부합니다.
2. 지급대상금액은 전일 정산의 이월금액과 당일 구매 확정된 지급대상의 합에서 다음 각 호의 금액을 차감한 금액으로 계산합니다.
① 이미 그 판매대금이 정산, 지급 되었으나 반품으로 인하여 정산 대상 기간 중에 고객에게 환불 처리된 상품의 판매금액에서 판매이용료를 차감한 금액의 합
② 제17조의 규정에 따라 결정된 제반 이용료의 합
③ 제20조 제5항의 규정에 따라 결정된 전담택배 이용료의 합
④ 제21조 제4항의 규정에 따라 결정된 반품 및 교환 운반비용의 합
⑤ 제25조의 규정에 따른 정산 유보액
⑥ 제34조 제7항의 규정에 따라 결정된 분쟁비용 및 영업손실금의 합
3. 구매확정이란 판매자가 발송된 상품에 대하여 구매자가 구매확정의 의사표시를 전자메일등 케이마켓(쇼핑몰 사업부문)이 제공하는 양식을 통하여 밝히는 것을 말합니다. 단 제21조 1항 본조의 기한까지 구매확정의사를 표시하지 않을 경우 그 익일부터 케이마켓(쇼핑몰 사업부문)은 자동 구매확정 처리할 수 있습니다.
4. 케이마켓(쇼핑몰 사업부문)은 입금대상금액을 판매자 매니저를 통하여 판매자에 통보합니다. 판매자는 익월5일 이내에 세금계산서를 발급받고 케이마켓(쇼핑몰 사업부문)에 입금대상금액의 지급을 청구합니다. 만약 케이마켓(쇼핑몰 사업부문)이 통보한 날로부터 5일 이내에 판매자가 세금계산서를 발급 받지 않은 경우, 당월 정산대금이 유보될 수 있습니다. 다만 개인 판매자의 경우에는 세금계산서를 발급 받지 않습니다.
5. 입금대상금액이 음의 금액일 경우에는 이를 차기 정산으로 이월합니다. 단 2기 연속으로 입금대상금액이 음의 금액일 경우에는 케이마켓(쇼핑몰 사업부문)은 판매자에 해당 금액의 별도 입금을 요청할 수 있으며 입금이 완료될 때까지 상품의 판매를 중지할 수 있습니다.
제25조 (정산의 유보)
1. 케이마켓(쇼핑몰 사업부문)은 판매자의 귀책사유로 인해 발생한 비용을 판매대금 정산 시 공제할 수 있으며, 판매자와의 이용계약 종료 후 당해 판매자의 판매대금의 일정 비율에 해당하는 금액을 이용계약 종료일로부터 일정기간 동안 예치하여 동 기간 동안 구매자로부터의 환불, 교환 등 클레임 제기 시 관련 비용의 지급에 사용할 수 있습니다.
2. 케이마켓(쇼핑몰 사업부문)은 장기간 배송지연 건을 배송완료 건으로 간주하여 주문 절차를 종결할 수 있되, 판매대금의 정산은 향후 구매자의 환불 요청에 대비하여 일정기간 유보할 수 있습니다.
3. 판매자의 채권자의 신청에 의한 판매대금의 가압류, 압류 및 추심명령 등 법원의 결정이 있을 경우, 케이마켓(쇼핑몰 사업부문)은 판매자와 채권자 간의 합의 또는 채무 액의 변제 등으로 동 결정이 해제될 때까지 판매대금의 정산을 중지할 수 있습니다.
4. 본 조에 정한 외에도 법률의 규정에 의하거나 합리적인 사유가 있는 경우(제15조 판매자의 의무위반 조항 포함) 에는 케이마켓(쇼핑몰 사업부문)은 판매자에게 제10조에 따라 통지하고 판매대금의 전부 또는 일부에 대한 정산을 일정 기간 유보할 수 있습니다.
5. 케이마켓는 아래의 사유가 발생하는 경우 상품판매대금의 정산을 유보할 수 있습니다.
① 회사는 판매회원이 매매 부적합 상품의 판매회원으로 적발되거나, 구매자 클레임의 다수 발생으로 인한 환불, 교환 등의 요청이 염려되는 경우 3개월간 정산을 보류할 수 있습니다.
② 판매회원과 구매자 간에 동일한 유형의 클레임이 지속적으로 발생하는 경우, 구매자로부터의 클레임에 대비하여 일정기간 판매대금의 정산을 유보할 수 있습니다.
③ 판매회원은 폐업신고 이후에는 원칙적으로 판매가 금지되며, 회사는 판매회원의 폐업 이후에 판매된 상품의 판매대금에 관해서는 정산을 하지 않는 것을 원칙으로 합니다.
④ 본 조에 정한 외에도 법률의 규정에 의하거나 합리적인 사유가 있는 경우에는 회사는 판매회원에게 통지하고 판매대금의 전부 또는 일부에 대한 정산을 일정 기간 유보할 수 있습니다.
제26조 (판매자의 통지의무)
1. 판매자는 다음 각 호에 해당하는 사실이 발생한 경우에는 지체 없이 그 내용을 판매자매니저를 통해 변경하여야 하며 사업자 등록증 등 관련문서를 케이마켓(쇼핑몰 사업부문)에 서면으로 제출하여야 합니다.
① 주소,상호,대표자 등 사업자 등록증에 명시된 사항이 변경된 경우
② 통장계좌 등의 변경이 있는 경우
③ 자본구성에 중대한 변경이 있는 경우
④ 개인사업자가 법인으로 변경되거나 법인이 개인사업자로 변경된 경우
⑤ 기타 연락전화 및 휴대전화, 전자우편주소 등의 변경이 있는 경우
2. 이의 지연 및 누락으로 인해 발생한 모든 문제에 대하여는 판매자가 책임을 집니다.
제27조 (비밀유지)
1. 각 당사자는 법령상 요구되는 경우를 제외하고는 상대방으로부터 취득한 고객 개인정보, 기술정보, 생산 및 판매계획, 노하우 등 비밀로 관리되는 정보를 제3자에게 누설하여서는 아니 되고 그 정보를 본 계약 이외의 목적으로 이용하여서는 아니 됩니다.
2. 판매자는 판매를 통해 취득한 케이마켓(쇼핑몰 사업부문)의 고객에 대한 정보를 이용하여 당해 판매건과 무관하게 유무선상으로 고객에게 접촉하거나 직거래를 통해 상품을 판매하거나 이를 시도하는 등의 행위를 하여서는 아니됩니다.
3. 판매자는 케이마켓(쇼핑몰 사업부문) 판매자매니저의 관리자 ID와 비밀번호의 보안을 철저히 관리하여야 하며 이를 제3자에게 누설하여서는 아니 됩니다.
4. 제1항 및 제2항의 의무는 본 약관에 의한 계약관계가 종료된 후에도 3년간 존속합니다.
제28조 (납세관리대행 서비스)
1. 납세관리대행 서비스는 개인판매자의 사업자등록, 부가세신고 등의 편의를 위하여 부가가치세법령의 규정에 따라 제공되는 서비스입니다.
2. 납세관리대행 서비스는 회사에 개인정보를 제공하고 상품판매 행위를 하는 사업자가 아닌 개인판매자 회원에 대하여 납세관리의 편의를 목적으로 본 약관에 동의하신 판매자회원에게 납세관리대행 서비스를 제공합니다.
3. 납세관리 대행 서비스의 내용
① 회사는 납세관리대행 서비스의 적용 대상이 되는 회원을 위하여 납세관리인 선정(변경) 신고, 사업자 등록 신청, 부가가치세 확정신고 등을 대행합니다.
② 회사는 납세관리대행 서비스 적용대상인 개인회원을 갈음하여 관련 조세법령에 따라 현금영수증과 세금계산서를 발급할 수 있습니다.
* 예시 *
- [과세기간별 공급대가가 1200만원 미만의 경우] 회사는 관련 조세법령에 해당하는 회원의 사업자 등록의 신청, 납세관리인의 선정 및 신고 등을 대행할 수 있으며, 납세의무 이행을 위해 필요한 경우 회원의 주민등록번호 등 거래 정보를 국세청 등의 관련 기관에 제공할 수 있습니다.
- [과세기간별 공급대가가 1200만원 이상의 경우] 회사는 조세법령에 의한 회원의 납세의무 이행을 위해 필요한 경우 회원의 주민등록번호 등 거래 정보를 국세청 등의 관련 기관에 제공할 수 있습니다.
4. 납세관리대행 서비스의 대상
본 서비스는 과세기간별로 10회 이상 판매가 이루어지고 그 판매금액이 600만원 이상~1,200만원 미만 (납부의무 면제자)인 개인판매자를 대상으로 합니다.
다만, 본 서비스의 적용대상은 관련 조세법령 변경 또는 관련기관의 요청에 따라 변경될 수 있습니다.
5. 회사는 납세관리대행 서비스 적용대상이 되는 개인판매자가 본약관의 적용일 이후 판매행위를 시작함에도 본 약관규정에 대한 동의를 하지 않는 경우 해당 판매자의 거래건수, 거래금액, 정산금액 등의 내용을 회사의 관할 세무서에 제출할 수 있습니다.
6. 납세관리 등 납세의무에 관한 규정
① 회사의 e마켓플레이스 서비스를 통하여 판매행위를 하는 모든 판매자는 납세의무를 이행하여야 합니다. 다만, 회사는 개인판매자의 납세의무 편의를 위하여 납세관리 대행 서비스를 제공합니다.
② 회사의 납세관리 대행 서비스의 적용내용 및 그 대상은 부가가치세법 등 관련 조세법령에 따릅니다.
