제49조 (S-Money의 종류)
케이마켓가 인정하는 구매자 S-Money는 비현금성입니다
제50조 (S-Money의 취득)
① S-Money는 각종 이벤트의 당첨, 고객보상 등을 통하여 적립되나, 상품구매 후 마일리지의 방법으로 신규적립되지 않습니다. 이용계약이 종료되는 경우 S-Money는 자동적으로 소멸됩니다. 케이마켓가 S-Money를 제공하는 경우 이의 사용기간과 조건을 통지합니다.
② S-Money에는 "비가용성 S-Money"와 "가용성 S-Money"가 있습니다. 비가용성 S-Money는 적립된 이후 일정기간이 지나, 가용성 S-Money로 전환되어 현금과 동일하게 구매자가 사용할 수 있습니다.
제51조 (S-Money의 사용범위)
구매자는 구매자 S-Money로 케이마켓 및 인터넷 쇼핑몰에서 재화와 용역의 구매를 할 수 있습니다. 단, 구매자 S-Money는 케이마켓 관계회사 일부에서는 사용하실 수 없습니다.
제52조 (구매자 S-Money의 소멸)
