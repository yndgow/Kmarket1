[제 12장 I-Point]
제53조 (I-Point의 사용계약)
I-Point 사용계약은 케이마켓가 정한 본 약관에 사용신청자가 동의한다는 의사표시를 함으로써 성립합니다.
제54조 (I-Point의 취득)
"I-Point"는 케이마켓 및 포인트 가맹사이트의 개별 정책에 따라 미리 공지되는 상품구매, 이벤트, 이용후기, 서평, 관람후기 등을 통하여 이용자에게 포인트 적립 기회를 부여/적립됩니다.
제55조 (I-Point의 사용범위)
① 적립된 "I-Point"는 ㈜케이마켓와 포인트 가맹사이트의 정책에 따라 쇼핑, 게임, 이벤트 응모 등 다양한 방법으로 사용할 수 있습니다.
② 전 항에서 포인트를 사용할 수 있는 각 정책들은 각 가맹점의 영업정책에 따라 변경될 수 있으며, 사용법 등은 각 가맹점 사이트마다 이용자가 알아보기 쉽도록 명시합니다.
제56조 (I-Point의 환불 및 소멸)
① 이용자가 구매 등으로 적립받은 포인트를 사용한 후 해당 적립 원인이 취소(반품)되는 경우 이용자는 별도로 회사에 이를 반환해야 하며 적립된 포인트의 잔여분이 있는 경우 원칙적으로 자동차감 됩니다.
② 상품에 대한 취소 및 반품상황의 책임이 가맹점 혹은 판매자에게 있을 경우, 클레임이 확정되면 포인트는 다시 환불됩니다.
③ 적립된 포인트의 유효기간은 적립 시 고지된 바에 따라 7일부터 365일 내 특정되며, 유효기간 내에 사용되지 않은 포인트는 기간 만료시 자동삭제됩니다. (개정 2016.10.07)
④ 적립된 포인트가 법률에 위반하여 적립되었거나, 부정한 방법에 의한 적립 사실이 확인될 경우 이에 대한 객관적 소명이 이루어질때까지 포인트의 사용이 중지되거나, 소멸될수있습니다.
