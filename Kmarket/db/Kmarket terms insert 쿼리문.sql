제58조 (개별약관)
① 이 약관은 회사와 회원간에 성립되는 서비스이용계약의 기본약정입니다. 회사는 필요한 경우 특정 서비스에 관하여 적용될 사항(이하 "개별약관"이라고 합니다)을 정하여 미리 공지할 수 있습니다. 회원이 이러한 개별약관에 동의하고 특정 서비스를 이용하는 경우에는 개별약관이 우선적으로 적용되고, 이 약관은 보충적인 효력을 갖습니다.
② 회사는 필요한 경우 서비스 이용과 관련된 세부적인 개별내용(이용정책 등)을 정하여 회사의 사이트 등을 통하여 공지할 수 있습니다.
제59조 (연결 몰과 피연결 몰 간의 관계)
① 상위 몰과 하위 몰이 하이퍼 링크(예: 하이퍼 링크의 대상에는 문자, 그림 및 동화상 등이 포함됨) 방식 등으로 연결된 경우, 전자를 연결 몰(웹 사이트)이라고 하고 후자를 피연결 몰(웹사이트) 이라고 합니다.
② 연결몰은 피연결몰이 독자적으로 제공하는 재화ㆍ용역에 의하여 이용자와 행하는 거래에 대해서 보증 책임을 지지 않는다는 뜻을 연결 몰의 사이트에서 명시한 경우에는 그 거래에 대한 보증 책임을 지지 않습니다.
제60조 (저작권의 귀속 및 이용제한)
① 회사에 등록된 게시물의 저작권은 제3자의 권리를 침해하지 않는 한, 게시자에게 속합니다.
