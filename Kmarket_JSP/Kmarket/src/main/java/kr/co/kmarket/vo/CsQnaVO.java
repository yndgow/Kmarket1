package kr.co.kmarket.vo;

public class CsQnaVO {
	private String uid;
	private String qc1Name;
	private String qc2Name;
	private String qnaTitle;
	private String qnaContent;
	private String regip;
	private String wdate;
	private String qnaCond;
	private int qnaNo;
	
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getQnaTitle() {
		return qnaTitle;
	}
	public void setQnaTitle(String qnaTitle) {
		this.qnaTitle = qnaTitle;
	}
	public String getQnaContent() {
		return qnaContent;
	}
	public void setQnaContent(String qnaContent) {
		this.qnaContent = qnaContent;
	}
	public String getRegip() {
		return regip;
	}
	public void setRegip(String regip) {
		this.regip = regip;
	}
	public String getWdate() {
		return wdate;
	}
	public void setWdate(String wdate) {
		this.wdate = wdate;
	}
	public String getQc1Name() {
		return qc1Name;
	}
	public void setQc1Name(String qc1Name) {
		this.qc1Name = qc1Name;
	}
	public String getQc2Name() {
		return qc2Name;
	}
	public void setQc2Name(String qc2Name) {
		this.qc2Name = qc2Name;
	}
	public String getQnaCond() {
		return qnaCond;
	}
	public void setQnaCond(String qnaCond) {
		this.qnaCond = qnaCond;
	}
	public int getQnaNo() {
		return qnaNo;
	}
	public void setQnaNo(int qnaNo) {
		this.qnaNo = qnaNo;
	}
	public void setQnaNo(String qnaNo) {
		this.qnaNo = Integer.parseInt(qnaNo);
	}
	
}
